    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        int fila = tblproveedores.getSelectedRow();
       if(fila==-1&&txtid.getText().isEmpty()){
            //JOptionPane.showMessageDialog(null,"Selecciona un cliente");
            MenuPrincipal m= new MenuPrincipal();
            m.warning("Selecciona un cliente");
       }else{
           p.setIdProveedores(Integer.parseInt(txtid.getText()));
           p.setNombre(txtnombre.getText());
           p.setApellido(txtapellido.getText());
           p.setDocumento(txtdocumento.getText());
           p.setRsocial(txtrsocial.getText());
           p.setDireccion(txtdireccion.getText());
           p.setTelefono(txttelefono.getText());
           p.setCorreo(txtcorreo.getText());
           
           if(dao.editar(p)){
               //JOptionPane.showMessageDialog(null,"Se modifico con exito");
               
               MenuPrincipal m= new MenuPrincipal();
               m.exito("Se modifico con exito");
               
               limpiarTablaProveedor();
               listarProveedor();
               limpiarCampos();
           }else{
               MenuPrincipal m = new MenuPrincipal();
               m.mistake("error al modificar proveedor");
           }
           
       }
       

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if(!txtid.getText().isEmpty()){
            int confirmacion=JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar el proveedor?","Confirmar",2);
            if(confirmacion==0){
                p.setIdProveedores(Integer.parseInt(txtid.getText()));
                dao.eliminar(p);
                limpiarTablaProveedor();
                listarProveedor();
                limpiarCampos();
                //JOptionPane.showMessageDialog(null,"Se elimino con exito");  
                
                MenuPrincipal m= new MenuPrincipal();
                m.exito("Se elimino con exito al proveedor");               
                
            }    
        }else{
                //JOptionPane.showMessageDialog(null,"Seleccione un cliente");
                MenuPrincipal m= new MenuPrincipal();
                m.warning("Seleccione un proveedor"); 
                
                
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        p.setDocumento(txtdocumento.getText());
        if(dao.buscarDocumento(p)){

            txtid.setText(p.getIdProveedores() + "");
            txtnombre.setText(p.getNombre());
            txtapellido.setText(p.getApellido());
            txtdocumento.setText(p.getDocumento());
            txtdocumento.setText(p.getRsocial());
            txtdireccion.setText(p.getDireccion());
            txttelefono.setText(p.getTelefono());
            txtcorreo.setText(p.getCorreo());   
        }else{
            //JOptionPane.showMessageDialog(null,"El cliente no existe");
            
            MenuPrincipal m= new MenuPrincipal();
            m.mistake("El proveedor no existe");
            limpiarCampos();
        }
   
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        p.setNombre(txtnombre.getText());
        p.setApellido(txtapellido.getText());
        p.setDocumento(txtdocumento.getText());
        p.setRsocial(txtcorreo.getText());
        p.setDireccion(txtdireccion.getText());
        p.setRsocial(txtcorreo.getText());
        p.setTelefono(txttelefono.getText());
        p.setCorreo(txtcorreo.getText());
        
        if(dao.insertar(p)){
            //JOptionPane.showConfirmDialog(null, "Cliente registrado con exito");
            MenuPrincipal m= new MenuPrincipal();
            m.exito("Proveedor registrado con exito");
            limpiarCampos();
            limpiarTablaProveedor(); 
            listarProveedor();
        }
        else{
            //JOptionPane.showConfirmDialog(null, "No se pudo registrar al cliente");           
            MenuPrincipal m= new MenuPrincipal();
            m.mistake("No se pudo registrar al proveedor");
            
            
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

